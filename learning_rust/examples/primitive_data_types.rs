// Primitive data types examples in Rust
fn main() {
    // Integer
    let int_val: i32 = 42;
    println!("Integer: {}", int_val);

    // Floating point
    let float_val: f64 = 3.1415;
    println!("Float: {}", float_val);

    // Boolean
    let is_active: bool = true;
    println!("Boolean: {}", is_active);

    // Character
    let letter: char = 'R';
    println!("Character: {}", letter);

    // Unsigned integer
    let unsigned: u8 = 255;
    println!("Unsigned Integer: {}", unsigned);
}
